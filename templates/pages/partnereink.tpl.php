<h2>
    <i class="fa fa-plug" aria-hidden="true"></i>
    Széchényi Programiroda Nonprofit Kft.
</h2>
<img src="images/partner/szechenyi.jpg" width="300px" class="float-right" style="margin-left:10px;" />
<p>
A Széchenyi Programirodák országos hálózatát a Kormány az Új Széchenyi Tervben meghirdetett pályázóközpontúság, minőség és hatékonyság elveinek megvalósulása érdekében hozta létre 2011-ben. Feladata az Új Széchenyi Terv biztosította források elosztásának elősegítése, a potenciális és aktív pályázók, kedvezményezettek térítésmentes tájékoztatása, szakmai támogatása a pályázati folyamat teljes tartama alatt. Az irodahálózat fenti projektben meghatározott tevékenységének célja a rendelkezésre álló források lehívásának hatékonyabbá tétele, valamint a kis- és középvállalkozások, civil szervezetek és önkormányzatok pályázati rendszerbe történő nagyobb arányú belépésének elősegítése.
</p>
