<h2>
    <i class="fa fa-plug" aria-hidden="true"></i>
    Megújuló Energiapark, Debrecen
</h2>
<img src="images/partner/energiapark_db.png" width="300px" class="float-right" style="margin-left:10px;" />
<p>
A bemutatópark többek között hivatott megmutatni a napkollektor működését, a napelemek működését valamint a hőszivattyús fűtési rendszert és a vízteres cserépkályhát. A különböző napelem típusok (pl. monokristályos napelem) és napkollektor típusok (pl. vákuumcsöves napkollektor) egymással való összehasonlítása remek lehetőség azoknak, akik ezt a témát tűzték ki kutatási célukként.
</p><p>
Előfordulhat, hogy a napelemes rendszer vagy napkollektoros rendszer ára túl magas az önerőből való finanszírozáshoz, ehhez nyújt segítséget a megújuló energia támogatása, a megújuló energiához kapcsolódó pályázatok. A cég a megújuló energiaforrásokhoz kötődő pályázatírással és az ehhez szükséges energetikai tanúsítvány elkészítésével is foglalkozik, illetve a kész rendszer folyamatos ellenőrzéséhez szükséges távfelügyelet kiépítését is vállalja. 
</p>

