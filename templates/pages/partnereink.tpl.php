<h2>
    <i class="fa fa-plug" aria-hidden="true"></i>
    Energiaklub
</h2>
<img src="images/partner/ek_logo_web.jpg" width="300px" class="float-right" style="margin-left:10px;" />

<p>
Az Energia Klub Magyarországon egyedülálló módon immár 20 éve dolgozik egy új szemléletmód elterjesztésén. Kutatásaikkal, képzéseikkel és széles körű kommunikációval a cél, hogy mind az energiát termelők, mind a felhasználók, vagy éppen a politikai döntéshozók másként viszonyuljanak az energiához. Szűkös készleteink ésszerű felhasználása, a sokszínű helyi erőforrásokon alapuló gazdaság, vagy az energiapazarló fogyasztási szokások megváltoztatása, mind-mind része a megoldásoknak, melyet a szervezet munkájával támogat. Tevékenységük az energiahatékonyság, a megújuló erőforrások, az éghajlatvédelem, a hagyományos energiahordozók és az energiapolitika területeire összpontosul.
</p>
