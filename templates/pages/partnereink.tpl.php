ENERGY HUNGARY ENERGETIKAI ZRT.
</h2>
<img src="images/partner/energy_hungary_logo.png" width="300px" class="float-right" style="margin-left:10px;" />
<p>
Az energy hungary küldetése az, hogy minél nagyobb mértékű energiamegtakarítást és költségcsökkentést érjenek el partnereiknél, hogy azok kevesebb energiát használjanak fel, és ezt az energiát is kedvezőbb áron szerezzék be, mint eddig. Ez a kettős megközelítés a költségcsökkentésen kívül két nagyon fontos előnyt rejt magában: egyrészt a kevesebb elfogyasztott energia kevésbé szennyezi környezetünket, másrészt a hatékonyság hosszú távú versenyképességet és fenntarthatóságot biztosít ügyfeleiknek, legyenek akár cégek, intézmények vagy önkormányzatok.
