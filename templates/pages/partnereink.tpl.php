<h2>
    <i class="fa fa-plug" aria-hidden="true"></i>
    Európalánta Közhasznú Egyesület
</h2>
<img src="images/partner/babozd.png" width="300px" class="float-right" style="margin-left:10px;" />
<p>
A 2010-ben alakult Európalánta Közhasznú Egyesület kiemelkedően színvonalas projektje a „Bábozd Zöldre az Otthonod” Óvodai Környezeti Nevelési Program, amely a környezettudatos életmód üzenetét adja át óvodásoknak, pedagógusaiknak és szüleiknek. A projekt központi elemei a Kabóca Bábszínház közreműködésével bemutatott bábelőadások, melyeken keresztül az energiatakarékosság, a hulladékkezelés, a fenntartható közlekedés, a vízgazdálkodás és az élővilág értékeit és fontosságát ismerik meg az óvodások.
</p>
