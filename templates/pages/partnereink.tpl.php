<h2>
    <i class="fa fa-plug" aria-hidden="true"></i>
    Termál - Egészségipari Klaszter
</h2>
<img src="images/partner/eszakklaszter.jpg" width="300px" class="float-right" style="margin-left:10px;" />
<p>
Az Észak-Alföldi Termál Klaszter Egyesület 2005. szeptember 22-én alakult meg 18 alapító taggal. Legfőbb célja, hogy elősegítse a termál- és gyógyturizmus érdekeltjeinek aktív részvételével az Észak-alföldi régió termál-, és gyógy-idegenforgalmának fejlődését, továbbá kezdeményezésekkel, javaslatokkal kívánja segíteni a termál- és gyógyturizmus résztvevőit. Ennek érdekében információs és egyeztető fórumot működtet, állásfoglalásokat készít a szakmapolitikai termálügyek megoldására és közreműködik a térség megyéinek turisztikai szempontú fejlesztési feladatainak összehangolásában is. Fontos célkitűzése, hogy tevékenységének eredményeként bővüljön a helyi turisztikai kínálat.
</p>
